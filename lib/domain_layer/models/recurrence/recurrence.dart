/// The recurrence a transaction can have
enum Recurrence {
  /// No recurrence
  none,

  /// Once
  once,

  /// Daily
  daily,

  /// Weekly
  weekly,

  /// Bi-weekly
  biweekly,

  /// Monthly
  monthly,

  /// Bi-monthly
  bimonthly,

  /// Quarterly
  quarterly,

  /// Yearly
  yearly,

  /// End of each month
  endOfEachMonth,
}
